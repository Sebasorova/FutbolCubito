CREATE TABLE IF NOT EXISTS TABLE_PREFIX_PLAYER
(
  id INTEGER PRIMARY KEY AUTO_INCREMENT,
  uuid CHAR(36) UNIQUE NOT NULL,
  name VARCHAR(16) NOT NULL,
  CONSTRAINT unique_uuid_cs UNIQUE (uuid)
);

CREATE TABLE IF NOT EXISTS TABLE_PREFIX_BLOCKBALL_STATS
(
  id INTEGER PRIMARY KEY AUTO_INCREMENT,
  shy_player_id INTEGER,
  wins INTEGER,
  games INTEGER,
  goals INTEGER,
  FOREIGN KEY (shy_player_id) REFERENCES TABLE_PREFIX_PLAYER(id),
  CONSTRAINT wins_small_games_cs CHECK (wins <= games)
)